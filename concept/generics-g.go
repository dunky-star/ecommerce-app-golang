package concept

import (
	"fmt"
	"reflect"
)

// Non Generics
func MainNonGenerics() {
	// Initialize a map for the integer values
	ints := map[string]int64{
		"first":  34,
		"second": 12,
	}

	// Initialize a map for the float values
	floats := map[string]float64{
		"first":  35.98,
		"second": 26.99,
	}

	fmt.Printf("Non-Generic Sums: %v and %v\n",
		SumInts(ints),
		SumFloats(floats))
}

// SumInts adds together the values of m.
func SumInts(m map[string]int64) int64 {
	var s int64
	for _, v := range m {
		s += v
	}
	return s
}

// SumFloats adds together the values of m.
func SumFloats(m map[string]float64) float64 {
	var s float64
	for _, v := range m {
		s += v
	}
	return s
}

// SumIntsOrFloats sums the values of map m. It supports both int64 and float64
// as types for map values. Declare the Number interface type to use as a type constraint.
type Number interface {
	int64 | float64
}

func SumNumbers[K comparable, V Number](m map[K]V) V {
	var s V
	for _, v := range m {
		s += v
	}
	return s
}

// Alternative
// Add function that works with any type that satisfies the Number constraint
func Add[T Number](a, b T) T {
	if reflect.TypeOf(a).Kind() == reflect.String || reflect.TypeOf(b).Kind() == reflect.String {
		panic("Add function does not support strings")
	}
	return a + b
}
